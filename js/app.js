/*==============================for success popup================================*/

// Function to show the popup
let popup = document.getElementById("successPopup");
    

// Function to open the popup
function openPopup() {
    popup.classList.add("open-popup");
}

// Function to close the popup
function closePopup() {
    popup.classList.remove("open-popup");
}

